    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (cboCategoria.getSelectedItem() == null
                    || cboCategoria.getSelectedItem().toString().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Escoja una Categoría");
                return;
            }

            String nombreCategoria = cboCategoria.getSelectedItem().toString();

            this.vistaReporte.setLayout(new BorderLayout());
            this.vistaReporte.removeAll();

            Map<String, Object> parametros = new HashMap<>();

            // La clave es "nombreCategoria" (¡con 'C' mayúscula!)
            parametros.put("nombrecategoria", nombreCategoria);

            JRViewer objReporte = new Reportes().reporteInterno("rp10_CarrascoVeraFabianAntonio.jasper", parametros);

            this.vistaReporte.add(objReporte);
            this.vistaReporte.revalidate();
            this.vistaReporte.repaint();
            objReporte.setVisible(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "ERROR en Reporte: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


