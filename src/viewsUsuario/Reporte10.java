    private void cargarCategorias() {
        try {
            DefaultComboBoxModel cbo = new DefaultComboBoxModel();

            ResultSet rs = li.listarCategoriasActivas();

            while (rs.next()) {
                String nombre_cat = rs.getString("nombrecategoria");
                cbo.addElement(nombre_cat);
            }

            cboCategoria.setModel(cbo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar las editoriales: " + e.getMessage());
        }
    }

