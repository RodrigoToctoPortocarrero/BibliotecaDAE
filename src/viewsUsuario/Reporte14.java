    private void CargarTitulos() {
        try {
            DefaultComboBoxModel cbo = new DefaultComboBoxModel();
            ResultSet rs = li.listarTitulosLibros();

            while (rs.next()) {
                String titu = rs.getString("titulo");
                cbo.addElement(titu);
            }

            cboTitulos.setModel(cbo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar los titulos de los libros: " + e.getMessage());
        }

    }

