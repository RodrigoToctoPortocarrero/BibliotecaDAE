    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        int fila = tblListadoEditoriales.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una Editorial de la tabla.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            // 1. OBTENER Y VALIDAR EL ESTADO DE VIGENCIA (√çndice 4: columna 'estado')
            String estadoEditorial = tblListadoEditoriales.getValueAt(fila, 4).toString();

            if (!estadoEditorial.equalsIgnoreCase("Activo")) {
                JOptionPane.showMessageDialog(this,
                        "Solo se pueden seleccionar Editoriales Activas (Estado: " + estadoEditorial + ").",
                        "Editorial Inactiva",
                        JOptionPane.WARNING_MESSAGE);
                return;
            }

            // 2. Obtener los datos (Solo se ejecuta si el estado es "Si")
            String idString = tblListadoEditoriales.getValueAt(fila, 0).toString(); // Columna 0: ID
            Integer idEditorial = Integer.parseInt(idString);
            String nombreEditorial = tblListadoEditoriales.getValueAt(fila, 1).toString(); // Columna 1: NOMBRE

            // 3. Llamar al m√©todo delegado del panel principal
            if (panelPrincipal != null) {
                panelPrincipal.setEditorialSeleccionada(idEditorial, nombreEditorial);

                // 4. Cerrar el JFrame contenedor
                if (frameContenedor != null) {
                    frameContenedor.dispose();
                }
            }

        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this,
                    "Error de formato de ID. El c√≥digo no es un n√∫mero v√°lido: " + nfe.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error al seleccionar la Editorial: " + e.getMessage(),
                    "Error General", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSeleccionarActionPerformed


