    private void btnDarbajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDarbajaActionPerformed
        try {

            if (txtId.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar un usuario para dar de baja.");
                return;
            }

            // Verificar si ya está dado de baja
            boolean vigente = chkvigente.isSelected();

            if (!vigente) {
                JOptionPane.showMessageDialog(this,
                        "El usuario ya está dado de baja.",
                        "Aviso",
                        JOptionPane.WARNING_MESSAGE);
                return;
            }

            // Confirmación de baja
            int op = JOptionPane.showOptionDialog(
                    null,
                    "¿Está seguro que desea dar de baja al usuario "
                    + txtNombre.getText() + " "
                    + txtApPaterno.getText() + "?",
                    "Confirmación de Baja",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE,
                    null,
                    new Object[]{"Sí", "No"},
                    "Sí"
            );

            if (op != JOptionPane.YES_OPTION) {
                return;
            }

            objUsuarios.darBajaUsuario(Integer.parseInt(txtId.getText()));

            JOptionPane.showMessageDialog(this, "Usuario dado de baja exitosamente.");

            limpiarControles();
            listarUsuarios();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_btnDarbajaActionPerformed

