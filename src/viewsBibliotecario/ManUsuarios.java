    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        try {
            if (btnNuevo.getText().equals("NUEVO")) {
                btnNuevo.setText("GUARDAR");
                limpiarControles();
                chkvigente.setEnabled(false);
                txtId.setEnabled(false);
                chkvigente.setSelected(true);
                jDateChooser1.setCalendar(null);
                txtId.setText(objUsuarios.generarCodigoUsuario().toString());
            } else if (btnNuevo.getText().equals("GUARDAR")) {

                if (txtNombre.getText().trim().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Debe ingresar el nombre");
                    return;
                }

                if (txtApPaterno.getText().trim().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Debe ingresar el apellido paterno");
                    return;
                }

                if (txtApMaterno.getText().trim().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Debe ingresar el apellido materno");
                    return;
                }

                if (jDateChooser1.getDate() == null) {
                    JOptionPane.showMessageDialog(this, "Debe seleccionar la fecha de nacimiento");
                    return;
                }

                Date fechaSeleccionada = jDateChooser1.getDate();
                Date hoy = new Date();

                SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
                fechaSeleccionada = formato.parse(formato.format(fechaSeleccionada));
                hoy = formato.parse(formato.format(hoy));

                // No puede ser futura
                if (fechaSeleccionada.after(hoy)) {
                    JOptionPane.showMessageDialog(this,
                            "La fecha de nacimiento no puede ser futura.",
                            "Fecha inválida",
                            JOptionPane.WARNING_MESSAGE);
                    jDateChooser1.requestFocus();
                    return;
                }

                // Mínimo 12 años
                long diferenciaMS = hoy.getTime() - fechaSeleccionada.getTime();
                long años = diferenciaMS / (1000L * 60 * 60 * 24 * 365);

                if (años < 12) {
                    JOptionPane.showMessageDialog(this,
                            "El usuario debe tener al menos 12 años.",
                            "Edad mínima",
                            JOptionPane.WARNING_MESSAGE);
                    jDateChooser1.requestFocus();
                    return;
                }

                if (txtUsuario.getText().trim().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Debe ingresar el usuario");
                    return;
                }

                if (txtContrasena.getText().trim().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Debe ingresar la contraseña");
                    return;
                }

                if (txtTelefono.getText().trim().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Debe ingresar el teléfono");
                    return;
                }

                // VALIDACIÓN DE TELÉFONO
                String telefono = txtTelefono.getText().trim();
                if (!telefono.matches("^[0-9]{9}$")) {
                    JOptionPane.showMessageDialog(this,
                            "El teléfono debe contener exactamente 9 dígitos.",
                            "Teléfono inválido",
                            JOptionPane.WARNING_MESSAGE);
                    txtTelefono.requestFocus();
                    return;
                }

                if (txtGmail.getText().trim().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Debe ingresar el correo");
                    return;
                }

                // VALIDACIÓN DE CORREO
                String correo = txtGmail.getText().trim();
                if (!correo.matches("^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,6}$")) {
                    JOptionPane.showMessageDialog(this,
                            "Debe ingresar un correo electrónico válido.",
                            "Correo inválido",
                            JOptionPane.WARNING_MESSAGE);
                    txtGmail.requestFocus();
                    return;
                }

                if (txtDireccion.getText().trim().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Debe ingresar la dirección");
                    return;
                }

                if (cboTipo.getSelectedIndex() == -1) {
                    JOptionPane.showMessageDialog(this, "Debe seleccionar un tipo de usuario");
                    return;
                }

                java.util.Date fechaUtil = jDateChooser1.getDate();
                java.sql.Date fechaNacimiento = new java.sql.Date(fechaUtil.getTime());

                int op = JOptionPane.showOptionDialog(
                        null,
                        "¿Está seguro que desea registrar al usuario "
                        + txtNombre.getText() + " "
                        + txtApPaterno.getText() + "?",
                        "Confirmación de Registro",
                        JOptionPane.YES_NO_OPTION,
                        JOptionPane.QUESTION_MESSAGE,
                        null,
                        new Object[]{"Sí", "No"},
                        "Sí"
                );

                if (op != JOptionPane.YES_OPTION) {
                    return; // usuario canceló
                }

                objUsuarios.registrar(Integer.valueOf(txtId.getText()),
                        txtNombre.getText(),
                        txtApPaterno.getText(),
                        txtApMaterno.getText(),
                        fechaNacimiento.toString(),
                        txtUsuario.getText(),
                        txtContrasena.getText(),
                        txtTelefono.getText(),
                        txtGmail.getText(),
                        txtDireccion.getText(),
                        cboTipo.getSelectedItem().toString()
                );

                JOptionPane.showMessageDialog(this, "Usuario registrado exitosamente.");

                limpiarControles();
                listarUsuarios();

                btnNuevo.setText("NUEVO");
            } else {
                btnNuevo.setText("NUEVO");
                limpiarControles();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

