    private void listarUsuarios() {
        ResultSet rsUsuarios = null;
        DefaultTableModel modeloU = new DefaultTableModel();

        // Columnas de la tabla
        modeloU.addColumn("ID");
        modeloU.addColumn("Nombre");
        modeloU.addColumn("Ap_Paterno");
        modeloU.addColumn("Ap_Materno");
        modeloU.addColumn("Fecha_Nacimiento");
        modeloU.addColumn("Usuario");
        modeloU.addColumn("Contraseña");
        modeloU.addColumn("Teléfono");
        modeloU.addColumn("Correo");
        modeloU.addColumn("Dirección");
        modeloU.addColumn("Tipo");
        modeloU.addColumn("Estado");

        tblUsuarios.setModel(modeloU);

        try {
            rsUsuarios = objUsuarios.listarUsuarios();

            while (rsUsuarios.next()) {

                String estado = rsUsuarios.getBoolean("estado") ? "Si" : "No";

                modeloU.addRow(new Object[]{
                    rsUsuarios.getInt("idusuario"),
                    rsUsuarios.getString("nombre"),
                    rsUsuarios.getString("ap_paterno"),
                    rsUsuarios.getString("ap_materno"),
                    rsUsuarios.getDate("f_nacimiento"),
                    rsUsuarios.getString("nomusuario"),
                    rsUsuarios.getString("contrasenia"),
                    rsUsuarios.getString("telefono"),
                    rsUsuarios.getString("correo"),
                    rsUsuarios.getString("direccion"),
                    rsUsuarios.getString("tipousuario"),
                    estado
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al listar usuarios: " + e.getMessage());
        }
    }

