    private void listarCategorias() {
        ResultSet rsCategorias = null;
        DefaultTableModel modelo = new DefaultTableModel();

        // Definici√≥n de las columnas de la tabla
        modelo.addColumn("ID");
        modelo.addColumn("NOMBRE");
        modelo.addColumn("DESCRIPCI√ìN");
        modelo.addColumn("ESTADO");

        try {
            // 1. Obtener los valores de los filtros
            String nombre = txtNombre.getText();
            String descripcion = txtDescripcion.getText();
            // String estadoSeleccionado = cbxEstado.getSelectedItem().toString(); // ‚ùå ELIMINADO

            // üí° REEMPLAZO: Establecer "Todos" como filtro fijo
            String estadoSeleccionado = "Todos";

            // 2. Llamar al nuevo m√©todo de filtrado
            String filtroEstado = estadoSeleccionado;

            // ‚ùå L√≥gica de conversi√≥n de estado eliminada
            /*
            if (estadoSeleccionado.equalsIgnoreCase("Activo")) {
                filtroEstado = "Activo"; // Corresponder√° a TRUE en la l√≥gica
            } else if (estadoSeleccionado.equalsIgnoreCase("Inactivo")) {
                filtroEstado = "Inactivo"; // Corresponder√° a FALSE en la l√≥gica
            } else {
                filtroEstado = "Todos";
            }
             */
            rsCategorias = objCategoria.filtrarCategorias(nombre, descripcion, filtroEstado);

            // 3. Llenar la tabla con los resultados
            while (rsCategorias.next()) {
                modelo.addRow(new Object[]{
                    rsCategorias.getInt("idcategoria"),
                    rsCategorias.getString("nombrecategoria"),
                    rsCategorias.getString("descripcion"),
                    rsCategorias.getBoolean("estado") ? "Activo" : "Inactivo"
                });
            }

            // 4. Asignar el modelo a la JTable y actualizar el contador
            tblListadoCategorias.setModel(modelo);
            txtTotalCategorias.setText(String.valueOf(tblListadoCategorias.getRowCount()));

            // 5. Cerrar el ResultSet (importante para evitar fugas de conexi√≥n)
            rsCategorias.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al listar categor√≠as: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

