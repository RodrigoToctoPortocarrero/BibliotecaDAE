    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        int fila = tblListadoCategorias.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una Categor√≠a de la tabla.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {

            String estadoCategoria = tblListadoCategorias.getValueAt(fila, 3).toString();

            if (!estadoCategoria.equalsIgnoreCase("Activo")) {
                JOptionPane.showMessageDialog(this,
                        "Solo se pueden seleccionar Categor√≠as Activas (Estado: " + estadoCategoria + ").",
                        "Categor√≠a Inactiva",
                        JOptionPane.WARNING_MESSAGE);
                return; // üõë DETIENE la ejecuci√≥n si no est√° Activo.
            }

            // 2. Obtener los datos (Solo se ejecuta si el estado es Activo)
            String idString = tblListadoCategorias.getValueAt(fila, 0).toString();
            Integer idCategoria = Integer.parseInt(idString); // Columna 0: ID
            String nombreCategoria = tblListadoCategorias.getValueAt(fila, 1).toString(); // Columna 1: NOMBRE

            // 3. Llamar al m√©todo delegado del panel principal
            if (panelPrincipal != null) {
                panelPrincipal.setCategoriaSeleccionada(idCategoria, nombreCategoria);

                // 4. Cerrar el JFrame contenedor
                if (frameContenedor != null) {
                    frameContenedor.dispose();
                }
            }
        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this,
                    "Error de formato de ID. El c√≥digo no es un n√∫mero v√°lido: " + nfe.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error al seleccionar la categor√≠a: " + e.getMessage(),
                    "Error General", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSeleccionarActionPerformed


