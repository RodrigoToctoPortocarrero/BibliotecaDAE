    private void listarAutor() {
        ResultSet rs = null;
        DefaultTableModel modelo = new DefaultTableModel();

        modelo.addColumn("Código");
        modelo.addColumn("Nombres");
        modelo.addColumn("A. Paterno");
        modelo.addColumn("A. Materno");
        modelo.addColumn("Descripción");
        modelo.addColumn("Vigente"); // ⭐ Nueva columna para el estado

        tblAutor.setModel(modelo);

        try {
            rs = autor.listarAutores();
            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getInt("idautor"),
                    rs.getString("nombres"),
                    rs.getString("apepaterno"),
                    rs.getString("apematerno"),
                    rs.getString("descripcion"),
                    rs.getBoolean("estado") // ⭐ Se obtiene el estado
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error al listar autores: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            try {
                if (rs != null) {
                    rs.close();
                }
            } catch (Exception e) {
                System.err.println("Error cerrando ResultSet: " + e.getMessage());
            }
        }
    }

    private void limpiarControles() {
        txtCodigo.setText("");
        txtAPaterno.setText("");
        txtDescripcion.setText("");
        txtAMaterno.setText("");
        txtNombres.setText("");
        chkVigente.setSelected(false);
        txtNombres.requestFocus();
    }
