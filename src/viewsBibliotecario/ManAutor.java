    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        try {
            if (txtCodigo.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar un autor para eliminar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
                return;
            }

            int confirm = JOptionPane.showConfirmDialog(this,
                    "¿Está seguro que desea eliminar (cambiar estado a Inactivo) al autor con código " + txtCodigo.getText() + "?",
                    "Confirmar Eliminación", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                // Asumiendo que eliminarAutor hace una eliminación lógica (cambiar estado a FALSE)
                autor.modificar(
                        Integer.parseInt(txtCodigo.getText()),
                        txtNombres.getText(),
                        txtAPaterno.getText(),
                        txtAMaterno.getText(),
                        txtDescripcion.getText(),
                        false // ⭐ Estado FALSE (Inactivo) al eliminar lógicamente
                );
                listarAutor();
                limpiarControles();
                JOptionPane.showMessageDialog(this, "Autor eliminado lógicamente (Estado: Inactivo) correctamente.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El código no es válido.", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar autor: " + e.getMessage(), "Error BD", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btneliminarActionPerformed

    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        try {
            if (btnNuevo.getText().equals("Nuevo")) {
                btnNuevo.setText("Guardar");
                limpiarControles();

                // Lógica para generar el nuevo código y forzar el estado a Vigente
                txtCodigo.setText(autor.generarCodigoAutor().toString());
                chkVigente.setSelected(true); // ⭐ Nuevo registro por defecto es Vigente

                txtCodigo.setEditable(false);
                txtNombres.requestFocus();
            } else {
                // Validaciones mínimas
                if (txtNombres.getText().isEmpty() || txtAPaterno.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Debe llenar nombres y apellido paterno.", "Advertencia", JOptionPane.WARNING_MESSAGE);
                    return;
                }

                // ⭐ Obtener el estado de vigencia del chkVigente
                int idAutor = Integer.parseInt(txtCodigo.getText());
                boolean estado = chkVigente.isSelected();
                autor.registrar(
                        idAutor, // <-- AÑADIDO: El ID que se generó y se mostró en el campo de texto
                        txtNombres.getText(),
                        txtAPaterno.getText(),
                        txtAMaterno.getText(),
                        txtDescripcion.getText(),
                        estado
                );
                JOptionPane.showMessageDialog(this, "Autor registrado correctamente.");
                btnNuevo.setText("Nuevo");
                listarAutor();
                limpiarControles();
                txtCodigo.setEditable(true); // Se vuelve editable para nueva busqueda/modificacion
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al registrar autor: " + e.getMessage(), "Error BD", JOptionPane.ERROR_MESSAGE);
            btnNuevo.setText("Nuevo");
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
        try {
            if (txtCodigo.getText().isEmpty() || txtNombres.getText().isEmpty() || txtAPaterno.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar un autor y llenar campos obligatorios.", "Advertencia", JOptionPane.WARNING_MESSAGE);
                return;
            }
            int id = Integer.parseInt(txtCodigo.getText());

            boolean estado = chkVigente.isSelected();

            autor.modificar(id,
                    txtNombres.getText(),
                    txtAPaterno.getText(),
                    txtAMaterno.getText(),
                    txtDescripcion.getText(),
                    estado
            );
            JOptionPane.showMessageDialog(this, "Autor modificado correctamente.");
            listarAutor();
            limpiarControles();
            txtCodigo.setEditable(true); // Se vuelve editable
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al modificar autor: " + e.getMessage(), "Error BD", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnmodificarActionPerformed

    private void tblAutorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAutorMouseClicked
        int fila = tblAutor.getSelectedRow();
        if (fila >= 0) {
            btnNuevo.setText("Nuevo");
            txtCodigo.setText(tblAutor.getValueAt(fila, 0).toString());
            txtNombres.setText(tblAutor.getValueAt(fila, 1).toString());
            txtAPaterno.setText(tblAutor.getValueAt(fila, 2).toString());
            txtAMaterno.setText(tblAutor.getValueAt(fila, 3).toString());
            txtDescripcion.setText(tblAutor.getValueAt(fila, 4).toString());

            // ⭐ Cargar el estado de vigencia del JTable al chkVigente
            // El estado está en la columna 5
            boolean estadoVigente = (boolean) tblAutor.getValueAt(fila, 5);
            chkVigente.setSelected(estadoVigente);
            txtCodigo.setEditable(true); // Habilita la edición del código para la búsqueda
        }
    }//GEN-LAST:event_tblAutorMouseClicked

