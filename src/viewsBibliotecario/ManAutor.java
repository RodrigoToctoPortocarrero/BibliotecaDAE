    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            if (txtCodigo.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Debe ingresar el código del autor para buscar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
                return;
            }

            int id = Integer.parseInt(txtCodigo.getText());
            ResultSet rs = autor.buscarAutor(id);
            if (rs.next()) {
                // Asumo que tu tabla Autor tiene columnas 'idautor', 'nombres', 'apepaterno', 'apematerno', 'descripcion' y 'estado' (boolean)
                txtCodigo.setText(rs.getString("idautor"));
                txtNombres.setText(rs.getString("nombres"));
                txtAPaterno.setText(rs.getString("apepaterno"));
                txtAMaterno.setText(rs.getString("apematerno"));
                txtDescripcion.setText(rs.getString("descripcion"));
                chkVigente.setSelected(rs.getBoolean("estado"));

                rs.close();
            } else {
                JOptionPane.showMessageDialog(this, "Autor no encontrado.", "Info", JOptionPane.INFORMATION_MESSAGE);
                limpiarControles();
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El código debe ser un número entero válido.", "Error", JOptionPane.ERROR_MESSAGE);
            txtCodigo.requestFocus();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al buscar autor: " + e.getMessage(), "Error BD", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

