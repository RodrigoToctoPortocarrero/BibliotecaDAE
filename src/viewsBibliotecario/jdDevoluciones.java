    private void btnGrabarDevolucionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarDevolucionActionPerformed
        if (modelo.getRowCount() == 0) return;

        try {
            cn.setAutoCommit(false); // Transacción

            // Recorremos la tabla fila por fila
            for (int i = 0; i < modelo.getRowCount(); i++) {
                
                int idPrestamo = Integer.parseInt(modelo.getValueAt(i, 0).toString());
                int idEjemplar = Integer.parseInt(modelo.getValueAt(i, 2).toString());
                long diasRetraso = Long.parseLong(modelo.getValueAt(i, 5).toString());
                String estadoLibro = modelo.getValueAt(i, 6).toString();
                boolean esBueno = estadoLibro.equalsIgnoreCase("Bueno");
                
                // 1. Verificar si ya existe cabecera de Devolución para este préstamo EN ESTA TRANSACCIÓN
                // (Para no crear 3 devoluciones si Juan devuelve 3 libros del mismo préstamo)
                int idDevolucion = obtenerIdDevolucionExistente(idPrestamo);
                
                if (idDevolucion == -1) {
                    // No existe devolución hoy para este préstamo, la creamos
                    idDevolucion = generarNuevoId("DEVOLUCION", "iddevolucion");
                    double multaTotal = diasRetraso * 5.00; // Logica simple de multa
                    
                    String sqlDev = "INSERT INTO DEVOLUCION (iddevolucion, idprestamo, fechadevolucionreal, multatotal, observaciones) VALUES (?, ?, ?, ?, ?)";
                    PreparedStatement pstDev = cn.prepareStatement(sqlDev);
                    pstDev.setInt(1, idDevolucion);
                    pstDev.setInt(2, idPrestamo);
                    pstDev.setDate(3, java.sql.Date.valueOf(LocalDate.now()));
                    pstDev.setDouble(4, multaTotal);
                    pstDev.setString(5, diasRetraso > 0 ? "Retraso" : "A tiempo");
                    pstDev.executeUpdate();
                    
                    // Si hubo multa, crear registro en tabla MULTA y bloquear usuario
                    if(multaTotal > 0) {
                         generarMultaYBloquear(idPrestamo, multaTotal);
                    }
                }

                // 2. Insertar Detalle Devolución
                int idDetalle = generarNuevoId("DETALLE_DEVOLUCION", "iddetalledev");
                String sqlDet = "INSERT INTO DETALLE_DEVOLUCION (iddetalledev, iddevolucion, idejemplar, estadoejemplar, multa) VALUES (?, ?, ?, ?, ?)";
                PreparedStatement pstDet = cn.prepareStatement(sqlDet);
                pstDet.setInt(1, idDetalle);
                pstDet.setInt(2, idDevolucion);
                pstDet.setInt(3, idEjemplar);
                pstDet.setBoolean(4, esBueno);
                pstDet.setDouble(5, esBueno ? 0.0 : 20.0); // 20 soles si está dañado (ejemplo)
                pstDet.executeUpdate();

                // 3. Liberar Ejemplar
                String sqlLib = "UPDATE EJEMPLAR SET disponible = TRUE WHERE idejemplar = ?";
                PreparedStatement pstLib = cn.prepareStatement(sqlLib);
                pstLib.setInt(1, idEjemplar);
                pstLib.executeUpdate();
                
                // 4. Cerrar Préstamo
                String sqlClose = "UPDATE PRESTAMO SET estado = FALSE WHERE idprestamo = ?";
                PreparedStatement pstClose = cn.prepareStatement(sqlClose);
                pstClose.setInt(1, idPrestamo);
                pstClose.executeUpdate();
            }

            cn.commit();
            JOptionPane.showMessageDialog(this, "Devoluciones procesadas con éxito.");
            modelo.setRowCount(0);
            txtFechaPrestamo.setText("");

        } catch (Exception e) {
            try { cn.rollback(); } catch (SQLException ex) {}
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        } finally {
            try { cn.setAutoCommit(true); } catch (SQLException ex) {}
        }
    }//GEN-LAST:event_btnGrabarDevolucionActionPerformed

