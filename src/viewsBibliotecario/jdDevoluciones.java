 // Método auxiliar para evitar duplicar cabeceras en el bucle
    private int obtenerIdDevolucionExistente(int idPrestamo) throws SQLException {
        // Busca si ya insertamos una devolución para este préstamo HOY en la BD
        String sql = "SELECT iddevolucion FROM DEVOLUCION WHERE idprestamo = ?";
        PreparedStatement pst = cn.prepareStatement(sql);
        pst.setInt(1, idPrestamo);
        ResultSet rs = pst.executeQuery();
        if(rs.next()) return rs.getInt(1);
        return -1;
    }
    // Método auxiliar para Multas (reutilizado de chats anteriores)
    private void generarMultaYBloquear(int idPrestamo, double monto) throws SQLException {
        // 1. Insertar en MULTA
        int idMulta = generarNuevoId("MULTA", "idmulta");
        // Ojo: necesitamos idusuario, lo sacamos con subconsulta rapida
        String sql = "INSERT INTO MULTA (idmulta, idusuario, idprestamo, monto, pagado, fechagenerada) " +
                     "SELECT ?, idusuariolector, ?, ?, FALSE, ? FROM PRESTAMO WHERE idprestamo = ?";
        PreparedStatement pst = cn.prepareStatement(sql);
        pst.setInt(1, idMulta);
        pst.setInt(2, idPrestamo);
        pst.setDouble(3, monto);
        pst.setDate(4, java.sql.Date.valueOf(LocalDate.now()));
        pst.setInt(5, idPrestamo);
        pst.executeUpdate();
        
        // 2. Bloquear Usuario
        String sqlBloq = "UPDATE USUARIO SET estado = FALSE WHERE idusuario = (SELECT idusuariolector FROM PRESTAMO WHERE idprestamo = ?)";
        PreparedStatement pstBloq = cn.prepareStatement(sqlBloq);
        pstBloq.setInt(1, idPrestamo);
        pstBloq.executeUpdate();
    }
    
    // Método para generar IDs (secuencial simple)
    private int generarNuevoId(String tabla, String campo) throws SQLException {
        ResultSet rs = cn.createStatement().executeQuery("SELECT MAX(" + campo + ") FROM " + tabla);
        if(rs.next()) return rs.getInt(1) + 1;
        return 1;
    }

    

