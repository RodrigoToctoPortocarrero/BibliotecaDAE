    private void btnBuscarPrestamo1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarPrestamo1ActionPerformed
       String fecha = txtFechaPrestamo.getText(); // Formato YYYY-MM-DD
        
        // Validar formato básico
        if(!fecha.matches("\\d{4}-\\d{2}-\\d{2}")) {
            JOptionPane.showMessageDialog(this, "Use formato: YYYY-MM-DD (Ej: 2025-05-01)");
            return;
        }

        // QUERY ACTUALIZADA: Busca por fecha y trae datos del lector
        String sql = "SELECT p.idprestamo, u.nombre || ' ' || u.ap_paterno AS lector, " +
                     "p.fechadevolucionestimada, dp.idejemplar, l.titulo " +
                     "FROM PRESTAMO p " +
                     "INNER JOIN USUARIO u ON p.idusuariolector = u.idusuario " +
                     "INNER JOIN DETALLE_PRESTAMO dp ON p.idprestamo = dp.idprestamo " +
                     "INNER JOIN EJEMPLAR e ON dp.idejemplar = e.idejemplar " +
                     "INNER JOIN LIBROS l ON e.idlibro = l.idlibro " +
                     "WHERE p.fechaprestamo = ? AND p.estado = TRUE"; // Solo activos

        try {
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setDate(1, java.sql.Date.valueOf(fecha)); // Convertir String a Date SQL
            ResultSet rs = pst.executeQuery();

            modelo.setRowCount(0);
            LocalDate hoy = LocalDate.now();
            boolean encontrado = false;

            while (rs.next()) {
                encontrado = true;
                
                // Calculamos retraso
                Date sqlFechaLimite = rs.getDate("fechadevolucionestimada");
                long dias = java.time.temporal.ChronoUnit.DAYS.between(sqlFechaLimite.toLocalDate(), hoy);
                if (dias < 0) dias = 0;

                modelo.addRow(new Object[]{
                    rs.getInt("idprestamo"),  // Columna 0: Vital para saber qué cerrar
                    rs.getString("lector"),   // Columna 1: Para ver de quién es
                    rs.getInt("idejemplar"),  // Columna 2
                    rs.getString("titulo"),
                    sqlFechaLimite.toString(),
                    dias,
                    "Bueno" // Estado por defecto
                });
            }

            if (!encontrado) {
                JOptionPane.showMessageDialog(this, "No se encontraron préstamos activos en esa fecha.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarPrestamo1ActionPerformed

