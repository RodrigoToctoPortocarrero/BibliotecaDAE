    private void CargarEditoriales() {
        try {
            DefaultComboBoxModel cbo = new DefaultComboBoxModel();

            ResultSet rs = ed.listarEditorialesActivas();

            while (rs.next()) {
                String nombre_ed = rs.getString("nombre");
                cbo.addElement(nombre_ed);
            }

            cboEditoriales.setModel(cbo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar las editoriales: " + e.getMessage());
        }
    }

    private void btnMostrarReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarReporteActionPerformed
        try {

            if (cboEditoriales.getSelectedItem() == null
                    || cboEditoriales.getSelectedItem().toString().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Escoja una Editorial");
                return;
            }

            String nombreEditorial = cboEditoriales.getSelectedItem().toString();

            this.contenedor.setLayout(new BorderLayout());
            this.contenedor.removeAll();

            Map<String, Object> parametros = new HashMap<>();

            parametros.put("NOMBRE_EDITORIAL", nombreEditorial);

            JRViewer objReporte = new Reportes().reporteInterno("reportePercy.jasper", parametros);

            this.contenedor.add(objReporte);
            this.contenedor.revalidate();
            this.contenedor.repaint();
            objReporte.setVisible(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "ERROR en Reporte: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnMostrarReporteActionPerformed


